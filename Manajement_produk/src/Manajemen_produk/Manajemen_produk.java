    private void btneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditActionPerformed
        try {
        st = connect.createStatement();
        String sql = "UPDATE tb_product SET " +
                     "product_name = '" + t_name.getText() + "', " +
                     "product_price = '" + t_harga.getText() + "', " +
                     "stock = '" + t_stok.getText() + "' " +
                     "WHERE Sku_product = '" + t_sku.getText() + "'";
        
        int rowsUpdated = st.executeUpdate(sql);
        if (rowsUpdated > 0) {
            JOptionPane.showMessageDialog(null, "Update Berhasil");
            TampilData("");
            reset();
            
            // Set ulang status tombol
            btnsave.setEnabled(true);
            btnedit.setEnabled(false);
            btndelete.setEnabled(false);
        } else {
            JOptionPane.showMessageDialog(null, "Gagal mengupdate data,SKU Tidak Sesuai");
        }
    } catch(Exception e) {
        e.printStackTrace();
    }
    }//GEN-LAST:event_btneditActionPerformed

    private void TB_MANAJEMENMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TB_MANAJEMENMouseClicked
       if (evt.getClickCount() == 1) {
        if (!isRowSelected) {
            int selectedRowIndex = TB_MANAJEMEN.getSelectedRow();
            TB_MANAJEMEN.setRowSelectionInterval(selectedRowIndex, selectedRowIndex);
            isRowSelected = true;

            t_sku.setText(TB_MANAJEMEN.getValueAt(selectedRowIndex, 0).toString());
            t_name.setText(TB_MANAJEMEN.getValueAt(selectedRowIndex, 1).toString());
            t_harga.setText(TB_MANAJEMEN.getValueAt(selectedRowIndex, 2).toString());
            t_stok.setText(TB_MANAJEMEN.getValueAt(selectedRowIndex, 3).toString());

            btnsave.setEnabled(false);
            btnedit.setEnabled(true);
            btndelete.setEnabled(true);
        } else {
            TB_MANAJEMEN.clearSelection();
            isRowSelected = false;

            t_sku.setText("");
            t_name.setText("");
            t_harga.setText("");
            t_stok.setText("");

            btnsave.setEnabled(true);
            btnedit.setEnabled(false);
            btndelete.setEnabled(false);
        }
       }
    }//GEN-LAST:event_TB_MANAJEMENMouseClicked

