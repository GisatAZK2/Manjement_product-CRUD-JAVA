    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
      String SKU=t_sku.getText();
      String NAMA=t_name.getText();
      String Harga=t_harga.getText();
      String Stok=t_stok.getText();
      
      try{
      st = connect.createStatement();
      String sql="select*from tb_product where Sku_product='"+SKU+"'";
      var cari=connect.prepareStatement(sql);
      rs=cari.executeQuery();
      if(rs.next()){
      JOptionPane.showMessageDialog(null,"Gagal Menyimpan Data,SKU Sudah Tersedia");
      }else{
      st.executeUpdate ("INSERT INTO tb_product (Sku_product,product_name,product_price,stock) VALUES ('"+SKU+"','"+NAMA+"','"+Harga+"','"+Stok+"')");
      TampilData("");
      JOptionPane.showMessageDialog(null,"Simpan Berhasil");
      t_sku.setText("");
      t_name.setText("");
      t_harga.setText("");
      t_stok.setText("");
      }
      }catch(Exception e){
      e.printStackTrace();
      }
    
    }//GEN-LAST:event_btnsaveActionPerformed

